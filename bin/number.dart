void main(List<String> args) {
  // INT untuk bilangan bulat
  // DOUBLE untuk bilangan decimal
  // NUM untuk bilangan dinamis, bisa berubah-ubah, bulat atau decimal

  int number1 = 10;
  double number2 = 10.5;
  // int number2 = 10.5; // Error: A value of type 'double' can't be assigned to a variable of type 'int'.
  print(number1);
  print(number2);

  // number1 = 1.0; // Error: A value of type 'double' can't be assigned to a variable of type 'int'.

  // NUM
  num number3 = 10;
  print(number3);
  number3 = 10.5;
  print(number3);
}
